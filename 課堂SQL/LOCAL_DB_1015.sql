SELECT * 
FROM STORE_INFORMATION 
WHERE SALES >= 1500;

-- AND 而且(嚴謹)
-- OR 或者(寬鬆)
SELECT * 
FROM STORE_INFORMATION 
WHERE SALES > 300 AND GEOGRAPHY_ID = 2;

SELECT * 
FROM STORE_INFORMATION 
WHERE SALES > 300 AND GEOGRAPHY_ID = 2 OR STORE_ID = 1;

-- Sales 高於 $1,000 或是 Sales 在 $500 及 $275 之間
SELECT * 
FROM STORE_INFORMATION 
WHERE SALES > 1000 OR (SALES > 275 AND SALES < 500);

