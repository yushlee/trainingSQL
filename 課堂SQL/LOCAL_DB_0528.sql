/*
1.「且」找出屬於西區的商店
2.「且」營業額大於300(包含300)
3.「且」商店名稱'L'開頭
4.「或」營業日介於2018年3月至4月
*/
SELECT STORE_ID,STORE_NAME,SALES, STORE_DATE,GEOGRAPHY_ID
FROM STORE_INFORMATION
WHERE GEOGRAPHY_ID = 2
AND SALES >= 300
AND STORE_NAME LIKE 'L%'
OR STORE_DATE BETWEEN '2018-03-01' AND '2018-04-30';








