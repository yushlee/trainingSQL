-- BETWEEN ... AND ...
-- 運用一個範圍 (range) 內 抓出資料庫中的值
SELECT STORE_ID,STORE_NAME,SALES
FROM STORE_INFORMATION
WHERE SALES BETWEEN 300 AND 1500;


SELECT STORE_ID,STORE_NAME,SALES, STORE_DATE
